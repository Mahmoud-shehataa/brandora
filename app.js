const toggleMenu = () => {
  const open = ids.mobilePanel.classList.toggle('is-open');
  ids.mobilePanel.setAttribute('aria-hidden', String(!open));
  ids.menuToggle.classList.toggle('is-open', open);
  ids.menuToggle.setAttribute('aria-expanded', String(open));
};

ids.langToggle.addEventListener('click', () => {
  const nextLang = document.documentElement.lang === 'ar' ? 'en' : 'ar';
  applyLanguage(nextLang);
});

ids.menuToggle.addEventListener('click', toggleMenu);

